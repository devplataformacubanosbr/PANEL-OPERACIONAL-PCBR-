-- =====================================================================
-- DASHBOARDOperacional-PCBR — standalone (single-tenant) schema
-- Reconstructed from the live Avante production schema
-- (project ref rcqkmaxkuxllcyjzqbvn), table-by-table, via
-- information_schema / pg_catalog introspection on 2026-07-13.
--
-- Changes vs. Avante:
--   1. organization_id (column + FK to organizaciones) removed from
--      every table that had it — this app is single-tenant.
--   2. clientes: campos_personalizados jsonb removed; the fields it
--      held now live in their own dedicated columns (see NOTE below —
--      most of them already existed as real columns in live Avante
--      *in addition to* campos_personalizados, so 002/migrate_data.mjs
--      merges both sources).
--   3. perfiles: organization_id removed.
--   4. RLS: every table gets a single permissive policy
--      (FOR ALL TO authenticated USING (true) WITH CHECK (true)),
--      matching the pattern already used for configuraciones_app in
--      database/migrations/sql_crear_configuraciones.sql.
--   5. New singleton table configuracion_empresa replaces the
--      branding columns that lived on organizaciones.
--
-- Auto-increment bigint PKs: Avante used a mix of plain sequences
-- (nextval(...) default) and identity columns (some ALWAYS, some BY
-- DEFAULT). All of them are normalized here to
-- `GENERATED BY DEFAULT AS IDENTITY` so that 002/migrate_data.mjs can
-- INSERT rows with their original Avante ids (to keep FK relationships
-- intact) without needing OVERRIDING SYSTEM VALUE. Run the
-- setval(...) calls at the bottom of this file AFTER loading data, or
-- rely on the ones already issued by migrate_data.mjs.
--
-- NOTE / FLAGGED FOR REVIEW:
--   - configuraciones_app in live Avante actually HAS an
--     organization_id column (PK was (clave, organization_id)), even
--     though the reference migration file used as a pattern for RLS
--     does not have it. Per instruction #1 ("quitar organization_id de
--     TODAS las tablas de arriba que la tengan") it is removed here
--     too, restoring PK to (clave) alone — matching the reference file.
--   - documentos_operacionales.dato_operacional_id is a bare bigint
--     with no FK in Avante (points at nothing) — kept as-is.
--   - Avante has several triggers/functions implementing business
--     logic (auto-updating clientes.valor_total, auto-org-id,
--     audit logging into historial_cambios/historial_clientes, and a
--     webhook trigger on mensajes that calls an Avante Edge Function
--     with a hardcoded service_role JWT). NONE of that is reproduced
--     here — only DDL was requested. If the standalone app needs any
--     of that behavior it must be re-implemented at the app layer or
--     as new triggers, reviewed independently (the webhook trigger in
--     particular should NOT be copied verbatim — it embeds a live
--     Avante secret).
-- =====================================================================

